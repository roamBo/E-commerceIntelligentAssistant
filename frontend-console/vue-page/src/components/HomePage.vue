// å¯¼èˆªåˆ°æŒ‡å®šé¡µé¢
const navigateTo = (page, e) => {
  console.log('HomePage: Navigating to', page);
  // é˜»æ­¢äº‹ä»¶å†’æ³¡ï¼Œé˜²æ­¢å¤šæ¬¡è§¦å‘
  if (e) {
    e.stopPropagation();
    e.preventDefault();
  }
  // æ·»åŠ å»¶æ—¶ä»¥ç¡®ä¿äº‹ä»¶ä¸ä¼šè¢«è¦†ç›–æˆ–ä¸¢å¤±
  setTimeout(() => {
    emits('change', page);
